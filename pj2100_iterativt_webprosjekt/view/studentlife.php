<p>Som student ved Westerdals er det alltid noe som skjer! Du har utallige muligheter både faglig og sosialt. Vi tilbyr et bredt spekter av utvalg og aktiviteter slik at du 
	lett blir kjent med dine medstudenter og kan engasjere deg i skolemiljøet.</p>

<p>For at du kan utnytte studietiden din til det ytterste har vi har totalt 16 utvalg som dekker de fleste behov. Alt fra Musikkutvalget til idrett –og korutvalget. 
Så bli medlem av et utvalg og meld deg på aktiviteter i dag!</p>

